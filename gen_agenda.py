def make_ts(y, m, d, h):
    return f"make_timestamptz({y}, {m}, {d}, {h}, 0, 0, 'Europe/Rome')"


def main():
    out = []
    out.append("BEGIN;")
    # Cancello soft-deletate del 2026 (per pulizia) e poi tutti i 2026
    out.append("DELETE FROM \"SwapCounterOffers\" WHERE \"SwapRequestId\" IN ("
               "SELECT \"Id\" FROM \"SwapRequests\" WHERE \"InitiatorShiftId\" IN ("
               "SELECT \"Id\" FROM \"Shifts\" WHERE EXTRACT(YEAR FROM \"Date\")=2026)"
               " OR \"CounterpartShiftId\" IN ("
               "SELECT \"Id\" FROM \"Shifts\" WHERE EXTRACT(YEAR FROM \"Date\")=2026));")
    out.append("DELETE FROM \"SwapRequests\" WHERE \"InitiatorShiftId\" IN ("
               "SELECT \"Id\" FROM \"Shifts\" WHERE EXTRACT(YEAR FROM \"Date\")=2026)"
               " OR \"CounterpartShiftId\" IN ("
               "SELECT \"Id\" FROM \"Shifts\" WHERE EXTRACT(YEAR FROM \"Date\")=2026);")
    out.append("DELETE FROM \"Shifts\" WHERE EXTRACT(YEAR FROM \"Date\")=2026;")

    cols = ('"Id","TenantId","Date","Code","StartUtc","EndUtc","MedicoTurnoId",'
            '"MedicoReperibileId","Status","IsDeleted","CreatedAtUtc"')
    for (m, d, kind_pdf, mdt, rep) in SHIFTS:
        code, sh, eh, overnight = KIND[kind_pdf]
        start = make_ts(2026, m, d, sh)
        if overnight:
            nxt = date(2026, m, d) + timedelta(days=1)
            end = make_ts(nxt.year, nxt.month, nxt.day, eh)
        else:
            end = make_ts(2026, m, d, eh)
        row = (
            f"INSERT INTO \"Shifts\" ({cols}) VALUES ("
            f"gen_random_uuid(),'{TENANT}','{2026:04d}-{m:02d}-{d:02d}','{code}',"
            f"{start},{end},'{DOC[mdt]}','{DOC[rep]}','Assigned',false,now());"
        )
        out.append(row)

    out.append("COMMIT;")
    sql = "\n".join(out)
    with open("agenda_2026.sql", "w", encoding="utf-8") as f:
        f.write(sql)
    print(f"OK: scritto agenda_2026.sql ({len(SHIFTS)} turni)")


if __name__ == "__main__":
    main()
